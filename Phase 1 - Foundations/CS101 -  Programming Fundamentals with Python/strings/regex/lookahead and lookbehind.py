# | Pattern    | Meaning             |
# | ---------- | ------------------- |
# | `(?=...)`  | Positive lookahead  | - means must match but don't include them
# | `(?!...)`  | Negative lookahead  | - characters must not match
# | `(?<=...)` | Positive lookbehind | - the previous charcater must match
# | `(?<!...)` | Negative lookbehind | - previous charcters must not match

# lookahead is placed at the end
# look behind is placed before the number
