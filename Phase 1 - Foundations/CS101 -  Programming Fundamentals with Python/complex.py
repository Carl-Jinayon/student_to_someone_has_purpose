# Complex Numbers

z = 3 + 4j

print(z)

print(type(z)) # Prints the type of the variable
  
print(z.real) # prints the real number
 
print(z.imag) # prints the imaginary parts

a = 2 + 3j 
b = 1 + 5j

print(a * b)

print(a / b)

abs(3 + 4j) # absolute value
print((3 + 4j).conjugate()) # Conjugate

# Python compares both the real and imaginary parts.

# A Python complex:

# Represents numbers of the form a + bj.
# Stores both a real and an imaginary component.
# Is immutable.
# Supports arithmetic operations.
# Is useful in scientific and engineering applications.
